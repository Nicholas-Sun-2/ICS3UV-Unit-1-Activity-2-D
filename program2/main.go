package main

import "fmt"

func main() {
	// Hail Mary Prayer

	fmt.Println("Hail, Mary, full of grace,")
	fmt.Println("the Lord is with thee.")
	fmt.Println("Blessed art thou amongst women")
	fmt.Println("and blessed is the fruit of thy womb, Jesus.")
	fmt.Println("Holy Mary, Mother of God,")
	fmt.Println("pray for us sinners,")
	fmt.Println("now and at the hour of our death.")
	fmt.Println("Amen.")

	fmt.Println("Done.")
}

